// Create a search index for fast lookups
export function searchCities(query: string, limit: number = 10): USCity[] {
  if (!query || query.length < 2) return [];
  
  const lowerQuery = query.toLowerCase();
  const results: USCity[] = [];
  const seen = new Set<string>();
  
  // First, prioritize exact prefix matches
  for (const city of US_CITIES) {
    if (city.city.toLowerCase().startsWith(lowerQuery)) {
      const key = `${city.city}|${city.state}`;
      if (!seen.has(key)) {
        seen.add(key);
        results.push(city);
        if (results.length >= limit) return results;
      }
    }
  }
  
  // Then, add cities that contain the query
  for (const city of US_CITIES) {
    if (city.city.toLowerCase().includes(lowerQuery) && !city.city.toLowerCase().startsWith(lowerQuery)) {
      const key = `${city.city}|${city.state}`;
      if (!seen.has(key)) {
        seen.add(key);
        results.push(city);
        if (results.length >= limit) return results;
      }
    }
  }
  
  return results;
}
